
# 练习3-2:问候语

names=['evan','echo','li']

# 两种列表使用方法：正序+倒序
print(f"Hello, {names[0].title()}, how are you?")
print(f"Hello, {names[1].title()}, how are you?")
print(f"Hello, {names[2].title()}, how are you?\n")

print(f"Hello, {names[-3].title()}, how are you?")
print(f"Hello, {names[-2].title()}, how are you?")
print(f"Hello, {names[-1].title()}, how are you?")